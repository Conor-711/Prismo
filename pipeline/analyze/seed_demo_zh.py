def main():
    c = sqlite3.connect(DB)
    np = nia = ncm = 0
    for pid, d in POSTS.items():
        if "title" in d:
            c.execute("UPDATE posts SET title_zh=? WHERE id=?", (d["title"], pid))
            np += c.total_changes and 1
        if "selftext" in d:
            c.execute("UPDATE posts SET selftext_zh=? WHERE id=?", (d["selftext"], pid))
        c.execute(
            "UPDATE item_analysis SET tldr_zh=?, bull_points_zh=?, bear_points_zh=? WHERE item_id=? AND item_type='post'",
            (d.get("tldr", ""), json.dumps(d.get("bull", []), ensure_ascii=False), json.dumps(d.get("bear", []), ensure_ascii=False), pid),
        )
        nia += 1
    for cid, body in COMMENTS.items():
        c.execute("UPDATE comments SET body_zh=? WHERE id=?", (body, cid))
        ncm += 1
    c.commit()
    print(f"seeded zh: posts={len(POSTS)} item_analysis={nia} comments={ncm}")


if __name__ == "__main__":
    main()
